<p align="justify">3. The balance of the rent together with the security deposit 
  (see clause 5) is payable not less than eight weeks before the start of the 
  rental period. If payment is not received by the due date, the Owners reserve 
  the right to give notice in writing that the reservation is cancelled. The client 
  will remain liable to pay the balance of the rent unless the Owners are able 
  to re-let the Property. In this event clause 6 of these booking conditions will 
  apply. Reservations made within eight weeks of the start of the rental period 
  require full payment at the time of booking.</p>
<p align="justify">4. Any chargeable expense arising during the rental period 
  (eg hire of linen) should be settled locally with the owners representative 
  before departure.</p>
<p align="justify">5.1 A security deposit of £300.00 is required in case of, for 
  example, damage to the property or its contents or excessive use of water or 
  electricity.</p>
<p align="justify">5.2 However, the sums reserved by these clauses 5.1 shall not 
  limit the Client's liability to the Owners (see also clause 6 below). The Owners 
  will account to the Clients for the security deposits and refund the balance 
  due within two weeks after the end of the rental period. </p>
<p align="justify">6. Subject to clauses two and three above, in the event of 
  a cancellation, refunds of amounts paid will be made if the Owners are able 
  to re-let the Property, and any expenses or losses incurred in so doing will 
  be deducted from the refundable amount. <b>The Client is strongly recommended 
  to arrange a comprehensive travel insurance policy (including cancellation cover) 
  and to have full cover for the party's personal belongings, public liability, 
  etc., since these are not covered by the Owners' insurance. The Client should 
  also be insured to cover any damage to the Property or its contents caused by 
  the Client's Party during the rental period.</b></p>
<p align="justify">7. The rental period shall commence at 4.00pm on the first 
  day and finish at 10.00am on the last day. The Owners shall not be obliged to 
  offer the accommodation before the time stated and the client shall not be entitled 
  to remain in occupation after the time stated.</p>
<p align="justify">8. The maximum number to reside in the Property shall not exceed 
  5 unless the Owners have given written permission. Pets are not allowed.</p>
<p align="justify">9. The Client agrees to be a considerate tenant and to take 
  good care of the Property and to leave it in a clean and tidy condition at the 
  end of the rental period. The rental price is inclusive of a final clean by 
  the Owners' representative but the Owners reserve the right to make a retention 
  from the security deposit to cover any additional cleaning costs if the Client 
  leaves the Property in an unacceptable condition. The Client also agrees not 
  to act in any way which would cause disturbance to those resident in neighbouring 
  properties.</p>
<p align="justify">10 The Client shall report to the Owner or to the Owners' agent 
